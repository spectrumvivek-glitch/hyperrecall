export interface FeatherProps {
  name: string;
  size?: number;
  color?: string;
  style?: StyleProp<TextStyle>;
}

export function Feather({ name, size = 24, color = "#000" }: FeatherProps) {
  const Icon = ICONS[name] ?? Circle;
  return <Icon size={size} color={color} strokeWidth={2} />;
}

Feather.font = {} as Record<string, number>;
Feather.glyphMap = Object.fromEntries(
  Object.keys(ICONS).map((k) => [k, 0]),
) as Record<string, number>;

export default Feather;
